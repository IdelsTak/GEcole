
package Models;

public class Appreciation {
    private int id_appr;
    private String contenu;
    private int ref_e;
    private int ref_inst;
    private int ref_module;

    public Appreciation() {
    }

    public int getId_appr() {
        return id_appr;
    }

    public void setId_appr(int id_appr) {
        this.id_appr = id_appr;
    }

    public String getContenu() {
        return contenu;
    }

    public void setContenu(String contenu) {
        this.contenu = contenu;
    }

    public int getRef_e() {
        return ref_e;
    }

    public void setRef_e(int ref_e) {
        this.ref_e = ref_e;
    }

    public int getRef_inst() {
        return ref_inst;
    }

    public void setRef_inst(int ref_inst) {
        this.ref_inst = ref_inst;
    }

    public int getRef_module() {
        return ref_module;
    }

    public void setRef_module(int ref_module) {
        this.ref_module = ref_module;
    }

    
}
